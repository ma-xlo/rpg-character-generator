export default class Character {

    setName(name) {
        this.name = name
    }

    setRace(race) {
        this.race = race
    }

    setClass(characterClass) {
        this.class = characterClass
    }

    setTrait(trait) {
        this.trait = trait
    }

    setQuote(quote) {
        this.quote = quote
    }

    setStory(story) {
        this.story = story
    }
    
    setImage(img) {
        this.img = img
    }
    
}
